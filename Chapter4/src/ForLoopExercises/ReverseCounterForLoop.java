package ForLoopExercises;

public class ReverseCounterForLoop {
    public static void main (String [] args) {

       /*
       Write a program that will use a for loop to output the numbers 50 to 1 (numbers in reverse)
        */

        for(int x = 50; x >= 1; x--) {
            System.out.println(x);
        }

    }

}
